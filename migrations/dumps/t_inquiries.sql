DROP TABLE IF EXISTS `t_inquiries`;

CREATE TABLE `t_inquiries` (
    `t_inquiry_id` int(10) unsigned NOT NULL AUTO_INCREMENT COMMENT 'T問合せID',
  `m_inquiry_type_id` int(10) unsigned NOT NULL COMMENT 'M問合せ種類ID',
  `m_inquiry_action_id` int(10) unsigned NOT NULL COMMENT 'M問合せアクションID',
  `subject` varchar(100) DEFAULT NULL COMMENT '件名',
  `body` text NOT NULL COMMENT '問合せ内容',
  `name` varchar(64) NOT NULL COMMENT '名前',
  `name_sei` varchar(30) DEFAULT NULL COMMENT '名前（姓）',
  `name_mei` varchar(30) DEFAULT NULL COMMENT '名前（名）',
  `kana_sei` varchar(30) DEFAULT NULL COMMENT 'カナ（姓）',
  `kana_mei` varchar(30) DEFAULT NULL COMMENT 'カナ（名）',
  `rental_no` varchar(255) DEFAULT NULL COMMENT '予約番号',
  `paypal_account` varchar(255) DEFAULT NULL COMMENT 'PayPalアカウント',
  `email` varchar(128) NOT NULL COMMENT 'メールアドレス',
  `phone` varchar(32) DEFAULT NULL COMMENT '電話番号',
  `m_customer_id` int(10) unsigned DEFAULT NULL COMMENT 'M顧客ID(お問合せユーザー)',
  `m_admin_id` int(10) unsigned DEFAULT NULL COMMENT 'M管理者ID(担当者)',
  `process_status` tinyint(3) unsigned DEFAULT '1' COMMENT '(ステータス) [1:未対応 2:対応中 3:対応済み 4:対応完了 5:保留 6:対応不要]',
  `process_deadline` date DEFAULT NULL COMMENT '対応期日',
  `process_priority` tinyint(3) unsigned NOT NULL DEFAULT '1' COMMENT '対応優先順[1:高 2:中 3:低]',
  `process_comment` varchar(1024) DEFAULT NULL COMMENT '対応内容備考',
  `create_date` date NOT NULL COMMENT '作成日',
  `create_time` time NOT NULL COMMENT '作成時',
  `create_admin_id` int(10) unsigned DEFAULT NULL COMMENT '作成管理ユーザーID',
  `update_date` date NOT NULL COMMENT '更新日時',
  `update_time` time NOT NULL COMMENT '更新時',
  `update_admin_id` int(10) unsigned DEFAULT NULL COMMENT '更新管理ユーザーID',
  `delete_flag` tinyint(3) unsigned NOT NULL DEFAULT '0' COMMENT '削除フラグ [0:未削除 1:削除済み]',
  PRIMARY KEY (`t_inquiry_id`),
  KEY `fk_m_inquiry_type_id_ti` (`m_inquiry_type_id`),
  KEY `fk_m_inquiry_action_id_ti` (`m_inquiry_action_id`),
  CONSTRAINT `fk_m_inquiry_action_id_ti` FOREIGN KEY (`m_inquiry_action_id`) REFERENCES `m_inquiry_actions` (`m_inquiry_action_id`),
  CONSTRAINT `fk_m_inquiry_type_id_ti` FOREIGN KEY (`m_inquiry_type_id`) REFERENCES `m_inquiry_types` (`m_inquiry_type_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='T問合せ';
